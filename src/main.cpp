#include "CPU.h"

int main()
{
    CPU cpu;
    std::vector<uint8_t> memory = {0x20, 0x09, 0x06, 0x20, 0x0c, 0x06, 0x20, 0x12, 0x06, 0xa2, 0x00, 0x60, 0xe8, 0xe0, 0x05, 0xd0, 0xfb, 0x60, 0x00};

    cpu.loadMemory(memory);
    cpu.memoryWrite(0xFFFC, 0x00);
    cpu.memoryWrite(0xFFFD, 0x06);
    cpu.reset();
    cpu.run();
    return 0;
}
